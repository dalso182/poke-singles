-- Teach search_products() to recognize the "N/M" query format
-- (e.g. "15/151", "166/151", "15 / 151"). When the trimmed query matches
-- ^(\S+)\s*/\s*(\d+)$, replace the substring ILIKE predicate with an
-- exact (card_number = N AND set_printed_total = M). Otherwise keep
-- today's behavior verbatim — bare numbers (e.g. "15") still get the
-- broad ILIKE substring match across search_text.
--
-- Signature unchanged: no client/RPC contract change. The previous 6-arg
-- definition is dropped and recreated so the body refresh is total.
--
-- Edge cases:
--   - Promo IDs like "SWSH001": no slash → regex skips → falls through
--     to the ILIKE branch.
--   - Secret rares "166/151": works (card_number='166' AND total=151).
--   - Leading-zero card_numbers (e.g. "015") would not match "15/151" by
--     design. TCGdex localId is unpadded for modern sets — if we ever
--     adopt a padded source, add lpad on both sides.

drop function if exists public.search_products(text, text, int, int, uuid[], uuid[]);

create or replace function public.search_products(
  q                text,
  sort             text default 'relevance',
  limit_n          int  default 60,
  offset_n         int  default 0,
  set_ids          uuid[] default null,
  p_card_type_ids  uuid[] default null
) returns setof public.products_search
language plpgsql stable security invoker as $$
declare
  qtrim     text := btrim(coalesce(q, ''));
  qpat      text := '%' || btrim(coalesce(q, '')) || '%';
  qprefix   text := btrim(coalesce(q, '')) || '%';
  qempty    bool := btrim(coalesce(q, '')) = '';
  has_sets  bool := set_ids is not null and array_length(set_ids, 1) > 0;
  has_types bool := p_card_type_ids is not null and array_length(p_card_type_ids, 1) > 0;
  m         text[];
  q_num     text;
  q_total   int;
  is_nm     bool := false;
begin
  m := regexp_match(qtrim, '^(\S+)\s*/\s*(\d+)$');
  if m is not null then
    q_num   := m[1];
    q_total := m[2]::int;
    is_nm   := true;
  end if;

  if sort = 'price-asc' then
    return query
      select * from public.products_search ps
      where (
        case
          when is_nm then ps.card_number = q_num and ps.set_printed_total = q_total
          else qempty or ps.search_text ilike qpat
        end
      )
        and (not has_sets or ps.set_id = any(set_ids))
        and (not has_types or ps.card_type_ids && p_card_type_ids)
      order by ps.price asc, ps.id asc
      limit limit_n offset offset_n;
  elsif sort = 'price-desc' then
    return query
      select * from public.products_search ps
      where (
        case
          when is_nm then ps.card_number = q_num and ps.set_printed_total = q_total
          else qempty or ps.search_text ilike qpat
        end
      )
        and (not has_sets or ps.set_id = any(set_ids))
        and (not has_types or ps.card_type_ids && p_card_type_ids)
      order by ps.price desc, ps.id asc
      limit limit_n offset offset_n;
  elsif sort = 'recent' then
    return query
      select * from public.products_search ps
      where (
        case
          when is_nm then ps.card_number = q_num and ps.set_printed_total = q_total
          else qempty or ps.search_text ilike qpat
        end
      )
        and (not has_sets or ps.set_id = any(set_ids))
        and (not has_types or ps.card_type_ids && p_card_type_ids)
      order by ps.last_restocked_at desc nulls last, ps.created_at desc, ps.id asc
      limit limit_n offset offset_n;
  else
    return query
      select * from public.products_search ps
      where (
        case
          when is_nm then ps.card_number = q_num and ps.set_printed_total = q_total
          else qempty or ps.search_text ilike qpat
        end
      )
        and (not has_sets or ps.set_id = any(set_ids))
        and (not has_types or ps.card_type_ids && p_card_type_ids)
      order by
        case
          when is_nm or qempty               then 99
          when ps.name ilike qprefix         then 0
          when ps.pokemon_name ilike qprefix then 1
          when ps.name ilike qpat            then 2
          else 3
        end asc,
        ps.last_restocked_at desc nulls last,
        ps.id asc
      limit limit_n offset offset_n;
  end if;
end;
$$;
